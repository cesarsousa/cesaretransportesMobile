package br.com.cesaretransportesMobile.adapter;


public class TextoMenuPrincipal {
	
	private String textoPrincipal;
	private String textoDetalhe;
	
	public TextoMenuPrincipal(String textoPrincipal, String textoDetalhe) {
		this.textoPrincipal = textoPrincipal;
		this.textoDetalhe = textoDetalhe;
	}

	public String getTextoPrincipal() {
		return textoPrincipal;
	}

	public String getTextoDetalhe() {
		return textoDetalhe;
	}	
}